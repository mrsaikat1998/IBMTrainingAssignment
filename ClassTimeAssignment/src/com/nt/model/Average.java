package com.nt.model;

public class Average {

	public static double avg(int a,int b) {
		double avg=(a+b)/2;
		return avg;
	}
	public static double avg(int a,int b,int c) {
		double avg=(a+b+c)/3;
		return avg;
	}
	public static double avg(double a,double b) {
		double avg=(a+b)/2;
		return avg;
	}
}
